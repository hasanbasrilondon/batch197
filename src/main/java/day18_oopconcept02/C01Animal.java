package day18_oopconcept02;

public class C01Animal {

    public String name;
    public int age;
    public boolean isPet;
    public C01Animal() {
    }
    public C01Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }
    public C01Animal(String name, int age, boolean isPet) {
        this.name = name;
        this.age = age;
        this.isPet = isPet;
    }

}
