package day09_loops;

public class C07DoWhileLoop01 {

    public static void main(String[] args) {

        /*
        While Loop:
        int i = 1;
        while( condition) {
        code;
        increment;
        }

        Do While:
        int i=1;
        do{
        code
        inc/dec;
        }
        While(Condition);
         */

        int i =1;
        while (i<1){
            System.out.println("I am a while loop....");
            i++;

        }

        int k =1;
        do {
            System.out.println("I am Do While loop....");
            k++;
        }while (k<1);








    }

}
