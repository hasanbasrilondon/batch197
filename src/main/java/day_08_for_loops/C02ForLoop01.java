package day_08_for_loops;

public class C02ForLoop01 {
    public static void main(String[] args) {
        // Type five times Hi on the console:

        // below structure is difficult to type, update and fix bugs in the code. And it has repeat

        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");
        System.out.println("Hi");

        // Java created loop structure to handle repeated action easily
        // There are four types of loops in Java:
        // 1) For Loop  2) while Loop 3) Do while Loop 4) For Each Loop

        // For Loop:
        // Starting value     condition   increment/decrement
        for (int i =1  ; i<6  ; i++){
            System.out.println("Hi");
        }
        // Example: 2 Types numbers from 3 to 12 on the console
    }

}
